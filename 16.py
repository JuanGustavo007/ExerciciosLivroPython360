""""
Escreva um programa que pede que o usuario de entrada em dois valores, em seguida, exiba em tela o resultado da soma,
subtração, multiplicacao e divisao desses numeros:
"""

valor_1 = int(input("Digite um valor: "))
valor_2 = int(input("Digite um segundo valor: "))

print("###################################")
print(f"Soma dos resultados {valor_1 + valor_2}")
print("###################################")
print(f"Subtração dos resultados {valor_1 - valor_2}")
print("###################################")
print(f"Multiplicacao dos resultados {valor_1 * valor_2}")
print("###################################")
print(f"Divisão dos resultados {valor_1 / valor_2}")

